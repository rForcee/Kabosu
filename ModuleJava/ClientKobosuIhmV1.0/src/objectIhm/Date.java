package objectIhm;

public class Date {
	
	private float heure;
	private int jour;
	
	
	
	
	public float getHeure() {
		return heure;
	}
	public void setHeure(float heure) {
		this.heure = heure;
	}
	public int getJour() {
		return jour;
	}
	public void setJour(int jour) {
		this.jour = jour;
	}

}
